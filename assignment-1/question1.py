a = 10
b = 3.5
c = 2 + 3j
d = True

print("Integer:", a, type(a))
print("Float:", b, type(b))
print("Complex:", c, type(c))
print("Boolean:", d, type(d))

print("\nArithmetic Operations:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Exponent:", a ** 2)

print("\nInteger Division:", a // b)
print("Modulus:", a % b)

print("\nComplex Operations:")
e = 1 + 2j
f = 3 + 4j
print("Complex Addition:", e + f)
print("Complex Multiplication:", e * f)
